import { FEAT } from '../common/world/features'

const testRoom = `
#############################################################################
#############################################################################
#############################################                    ############
############################################# ################## ############
############################################# ################## ############
############################################# ################## ############
#########################                             ########## ############
#########################                             ########## ############
#########################                             ########## ############
#########################                             ########## ############
###########                                           ########## ############
###### #### #############                             ########## ############
###### #### #############                             ########## ############
###### #### #############                             ########## ############
###### #### #############                             ########## ############
###### #### #############                             #######################
###### #### #############                             #######################
###### #### #################################################################
###### #### #################################################################
######      #################################################################
#############################################################################
#############################################################################
#############################################################################
`

export function loadTestRoom(): FEAT[][] {
  return testRoom
    .split('\n')
    .filter(line => line.length !== 0)
    .map(line => {
      return line.split('').map(cell => {
        return cell === '#' ? FEAT.GRANITE : FEAT.FLOOR
      })
    })
}
